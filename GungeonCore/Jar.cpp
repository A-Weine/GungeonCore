Jar::Jar(string filename, float x, float y)
{
    sprite = new Sprite(filename);
    
    BBox(new Rect(-(sprite->Width() / 2.0f), -(sprite->Height() / 2.0f), (sprite->Width() / 2.0f), (sprite->Height() / 2.0f)));

    MoveTo(x, y);
}

// ------------------------------------------------------------------------------

Jar::~Jar()
{
    delete sprite;
}

